package com.sure.oa.model;

public class Dep {

    private Integer d_id;
    private String d_name;
    private String d_remark;
    private Integer d_status;

    public Integer getD_id() {
        return d_id;
    }

    public void setD_id(Integer d_id) {
        this.d_id = d_id;
    }

    public String getD_name() {
        return d_name;
    }

    public void setD_name(String d_name) {
        this.d_name = d_name;
    }

    public String getD_remark() {
        return d_remark;
    }

    public void setD_remark(String d_remark) {
        this.d_remark = d_remark;
    }

    public Integer getD_status() {
        return d_status;
    }

    public void setD_status(Integer d_status) {
        this.d_status = d_status;
    }
}
